    private void tambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahkanActionPerformed
        // TODO add your handling code here:
        if(keranjang.getRowCount()!= 0){
            pesan.setVisible(true);
        }
        DefaultTableModel dtm = (DefaultTableModel)keranjang.getModel();
        Object tenant, noTelp, namaMenu, hargaMenu, jml;
        if(keranjang.getSelectionModel().isSelectionEmpty()){
            tenant = (String) restaurant.getValueAt(restaurant.getSelectedRow(), 0);
            noTelp = (String) restaurant.getValueAt(restaurant.getSelectedRow(), 1);
            namaMenu = (String) menu.getValueAt(menu.getSelectedRow(), 0);
            hargaMenu = (int) menu.getValueAt(menu.getSelectedRow(), 1) * Integer.valueOf(count.getText());
            jml = count.getText();
        }
        else{
            tenant = (String) keranjang.getValueAt(keranjang.getSelectedRow(), 0);
            noTelp = (String) keranjang.getValueAt(keranjang.getSelectedRow(), 1);
            namaMenu = (String) keranjang.getValueAt(keranjang.getSelectedRow(), 2);
            hargaMenu = (int) keranjang.getValueAt(keranjang.getSelectedRow(), 4) * Integer.valueOf(count.getText());
            jml = count.getText();
        }
        if(isExist(String.valueOf(noTelp), String.valueOf(namaMenu))!= -1)
        {
            int i = isExist(String.valueOf(noTelp), String.valueOf(namaMenu));
            keranjang.setValueAt(count.getText(), i, 3);
            keranjang.setValueAt(Integer.valueOf(count.getText())*(int)keranjang.getValueAt(i, 4), i, 4);
        }
        else
        {
            Object[] data = {tenant, noTelp, namaMenu, jml, hargaMenu};
            dtm.addRow(data);
        }
        this.lostFocus();
        View.refreshTable(menu);
        this.updateTable();
    }//GEN-LAST:event_tambahkanActionPerformed

    private void pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanActionPerformed
        // TODO add your handling code here:
        if(keranjang.getRowCount() == 0){
            JOptionPane.showMessageDialog(this, "Isi menu yang ingin dipilih!");
            return;
        }
        
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");  
        LocalDateTime now = LocalDateTime.now(); 
        
        int totalHarga = 0;
        ArrayList<Object> nt = new ArrayList<>();
        for(int i = 0; i < keranjang.getRowCount(); i++){
            totalHarga += (Integer)keranjang.getValueAt(i, 4);
            if(!nt.contains(keranjang.getValueAt(i,1))){
                nt.add(keranjang.getValueAt(i,1));
            }
        }
        
        if((Integer)HomeView.auth[5] < totalHarga){
            JOptionPane.showMessageDialog(this, "Saldo tidak cukup!");
            return;
        }
        
        Object[] ntelp = nt.toArray();
        Object[] data = {HomeView.auth[0], meja.getSelectedItem(), totalHarga, dtf.format(now)};
        o.makeOrder(data);
        
        for (Object ntelp1 : ntelp) {
            Object[] tenantsData = {t.getTenantId(String.valueOf(ntelp1)), o.newestOrder()};
            o.makeTenantOrders(tenantsData);
        }
        
        for(int i = 0; i <keranjang.getRowCount(); i++){
            Object[] dataMenu = {o.newestOrder(), t.getIdMenu(String.valueOf(
                    keranjang.getValueAt(i, 1)), String.valueOf(keranjang.getValueAt(i, 2))),
                    keranjang.getValueAt(i,3)};
            o.addOrderMenu(dataMenu);
        }
        new HomeView().setVisible(true);
        dispose();
    }//GEN-LAST:event_pesanActionPerformed

