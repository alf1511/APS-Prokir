    public int countValue(){
        int res = 0;
        DefaultTableModel a = (DefaultTableModel)keranjang.getModel();
        for(int i = 0; i < a.getRowCount(); i++){
            if(
               restaurant.getValueAt(restaurant.getSelectedRow(), 1).equals(a.getValueAt(i, 1)) 
                    &&
               menu.getValueAt(menu.getSelectedRow(), 0) .equals(a.getValueAt(i, 2))
               )
            {
                res = Integer.valueOf((String)a.getValueAt(i, 3));
            }
        }
        return res;
    }

    
    public int isExist(String noTlp, String namaMakanan){
        int num = -1;
        for(int i = 0; i < keranjang.getRowCount(); i++){
            if(
                    keranjang.getValueAt(i, 2).equals(namaMakanan) &&
                    keranjang.getValueAt(i, 1).equals(noTlp)
              )
            {
                return i;
            }
        }
        return num;
    }
    
    public void updateTable(){
        DefaultTableModel dtm = (DefaultTableModel)keranjang.getModel();
        for(int i = 0; i < keranjang.getRowCount(); i++){
            if(
                    Integer.valueOf((String) keranjang.getValueAt(i, 3)) == 0
              )
            {
                dtm.removeRow(i);
            }
        }
    }
    
                
