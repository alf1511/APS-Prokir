    private void topUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_topUpActionPerformed
        
        if(Validator.isNumeric(nominal.getText())){
            try {
                cc.TopUp((int)HomeView.auth[0], parseDouble(nominal.getText()));
                HomeView.auth[5] = this.cc.getSaldo((int) HomeView.auth[0]);
                saldo.setText("Saldo: "+HomeView.auth[5]);
                JOptionPane.showMessageDialog(this, "Top up berhasil.");
                nominal.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(HomeView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else JOptionPane.showMessageDialog(this, "Masukan nominal dengan tepat.");
    }//GEN-LAST:event_topUpActionPerformed

