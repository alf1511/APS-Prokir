    public HomeView() {
        initComponents();
        welcome.setText("Welcome "+HomeView.auth[1]);
        saldo.setText("Saldo: "+HomeView.auth[5]);
        finish.setVisible(false);
        
        try {
            order = cc.newestOrderCust((Integer)HomeView.auth[0]);
            if(order[4] == null)return;
            if(order[4].equals("Dalam proses") || order[4].equals("Berhasil")){
                if(order[4].equals("Berhasil")){
                    finish.setVisible(true);
                }
                restoHome.setVisible(true);
                keterangan.setText(String.valueOf(order[4]));
                ResultSet rest = oc.orderRestaurant((Integer)order[0]);
                View.showData(rest, restoHome);
            }
            else if(order[4].equals("Exit")){
                JOptionPane.showMessageDialog(this, "Pesanan anda gagal, harap mencoba kembali.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(HomeView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

